    private void loadListTable(){
        if(pnlShowTable.getComponentCount()> 0){
            pnlShowTable.removeAll();
            
            pnlShowTable.revalidate();
            pnlShowTable.repaint();
            
        }
        
        ArrayList<Table> listTable = TableDAO.instance().getAllListTable();
        
        for(Table x: listTable){
            JButton btn = new JButton(x.getName() + " \n" + x.getStatus());
            btn.setPreferredSize(new Dimension(Table.tableWidth, Table.tableHeight));
            
            if(!x.getStatus().equals("Trống")){
                btn.setBackground(Color.LIGHT_GRAY);
            }
            btn.addActionListener((e) -> {
                btnActionPerformed(e);
            });
            pnlShowTable.add(btn);
        }
    }
    private void loadListCategory(){
        ArrayList<Categorys> category = CategoryDAO.instence().getCategory();
        Collections.reverse(category);
        for(int i = 0; i< category.size();i++){
            listCategory.addItem(category.get(i).getNameCategory());
        }
    }
    
    private void loadListNameFood(int idCategory){
        if(listNameFood.getItemCount()>0){
            listNameFood.removeAllItems();
        }
        ArrayList<Food> ListNameFood = FoodDAO.instance().getListFoodByCategoryID(idCategory);
        for(int i = 0; i< ListNameFood.size(); i++){
            listNameFood.addItem(ListNameFood.get(i).getNameFood());
        }
    }
    private void loadListMenu(int idBill){
        tableListInFo.setModel(MenuDAO.instance().getListBillInFoBillID(idBill).getModel());
        double totalMoney = 0;
        int rowCount = tableListInFo.getRowCount();
        for(int i= 0; i< rowCount; i++){
            totalMoney +=(double) tableListInFo.getValueAt(i, 3);
        }
        tfTotalMoney.setText(Double.toString(totalMoney ));
    }
    private void loadListChangeTable() {
        ArrayList<Table> listTable = TableDAO.instance().getListTableStatus();
        
        for(int i = 0; i< listTable.size(); i++){
            listChuyenban.addItem(listTable.get(i).getName());
        }
    }
        
