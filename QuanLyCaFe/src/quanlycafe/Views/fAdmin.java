    private void tableListThucAnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableListThucAnMouseClicked
        // TODO add your handling code here:
        int rowSelect = tableListThucAn.getSelectedRow();
        String name = (String) tableListThucAn.getValueAt(rowSelect, 0);
       ArrayList<Food> listFood = FoodDAO.instance().getListFood();
       
       for(int i = 0; i< listFood.size(); i++)
           if(listFood.get(i).getNameFood().equals(name)){
               thucAnId.setText(Integer.toString(listFood.get(i).getId()));
               thucAnNameFood.setText(listFood.get(i).getNameFood());
               thucAnDanhMuc.setSelectedIndex(listFood.get(i).getIdCategory());
               thucAnGia.setValue(listFood.get(i).getPrice());
               break;
           }
       
       
    }//GEN-LAST:event_tableListThucAnMouseClicked

    private void thucAnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_thucAnSuaActionPerformed
        // TODO add your handling code here:
        
        Food fd = getFoodByInFo();
        ArrayList<Food> listFood = FoodDAO.instance().getListFood();
        
        if(checkInFoFood(fd))
            for(Food x : listFood){
                if(x.getId() == fd.getId()){
                    if(FoodDAO.instance().updateFood(fd)){
                        JOptionPane.showMessageDialog(rootPane, "Sửa sản phẩm thành công");
                    }else JOptionPane.showMessageDialog(rootPane, "Serve Sửa không thành công");
                    loadFoodList();
                    resetInFoFood();
                    return;
                }
            } JOptionPane.showMessageDialog(rootPane, "Sửa không thành công\nSản Phẩm chưa tồn tại trong danh sách hàng.");
            
    }//GEN-LAST:event_thucAnSuaActionPerformed

    private void thucAnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_thucAnXoaActionPerformed
        // TODO add your handling code here:
        Food fd = getFoodByInFo();
        ArrayList<Food> listFood = FoodDAO.instance().getListFood();
        
        if(checkInFoFood(fd))
            for(Food x : listFood){
                if(x.getId() == fd.getId()){
                    if(FoodDAO.instance().deleteFood(fd)){
                        JOptionPane.showMessageDialog(rootPane, "Xóa sản phẩm thành công");
                    }else JOptionPane.showMessageDialog(rootPane, "Serve xóa không thành công");
                    loadFoodList();
                    resetInFoFood();
                    return;
                }
            } JOptionPane.showMessageDialog(rootPane, "Xóa không thành công\nSản Phẩm chưa tồn tại trong danh sách hàng.");
            
    }//GEN-LAST:event_thucAnXoaActionPerformed

    private void thucAnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_thucAnThemActionPerformed
        // TODO add your handling code here:
        Food fd = getFoodByInFo();
        ArrayList<Food> listFood = FoodDAO.instance().getListFood();
        
        if(checkInFoFood(fd)){
            for(Food xFood: listFood){
                if(xFood.getNameFood().equals(fd.getNameFood())){
                    int k = JOptionPane.showConfirmDialog(rootPane, "Tên sản phẩm đã tồn tại trong danh sách sản phẩm\nBan có muốn thêm mới không? ", "Thông báo xác nhận", JOptionPane.YES_NO_OPTION);
                    if(k == 0){
                        if(FoodDAO.instance().insertFood(fd)){
                            JOptionPane.showMessageDialog(rootPane, "Thêm sản phẩm thành công");
                        }else JOptionPane.showMessageDialog(rootPane, "Serve xóa không thành công");
                        loadFoodList();
                        resetInFoFood();
                    }
                    return;
                }
            }
            if(FoodDAO.instance().insertFood(fd)){
                JOptionPane.showMessageDialog(rootPane, "Thêm sản phẩm thành công");
                }else JOptionPane.showMessageDialog(rootPane, "Serve xóa không thành công");
                loadFoodList();
                resetInFoFood();
        }    
            
    }//GEN-LAST:event_thucAnThemActionPerformed

