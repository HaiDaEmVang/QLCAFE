    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        int idCategory   = listCategory.getSelectedIndex();
        int idFoodSelect = listNameFood.getSelectedIndex();
        ArrayList<Food> listFoodByCategory = FoodDAO.instance().getListFoodByCategoryID(idCategory);
        
        int idFood = listFoodByCategory.get(idFoodSelect).getId();
        
        int count = (int)countFood.getValue();
        
        int idBill = BillDAO.instance().GetUncheckBillIDByTableID(idTableClick);
        
        if(idCategory == 0 || idTableClick == 0 || (count <= 0 && MenuDAO.instance().getListBillInFoBillID(idBill).getRowCount() == 0))
            JOptionPane.showMessageDialog(rootPane, "Nhập lại lựa chọn!!");
        else{
            if(idBill == -1){
                BillDAO.instance().insertNewBillByIDTable(idTableClick);
                idBill = BillDAO.instance().getIDMaxBill();

            }
            BillInFoDAO.instance().insertBillInFo(idBill, idFood, count);
            TableDAO.instance().updateStatusTableByIDTable(idTableClick);

            loadListTable();

            loadListMenu(idBill);
        }
    }//GEN-LAST:event_btnAddActionPerformed
    
    private void btnActionPerformed(java.awt.event.ActionEvent evt) {
        String[] tablenumber = evt.getActionCommand().split(" ");
        idTableClick = Integer.parseInt(tablenumber[1]);
        int idBill = BillDAO.instance().GetUncheckBillIDByTableID(idTableClick);
        
        loadListMenu(idBill);
    }
    
    
    
