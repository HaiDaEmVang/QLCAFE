    private void loadListDanhMuc(){
        ArrayList<Categorys> category = CategoryDAO.instence().getCategory();
        Collections.reverse(category);
        for(int i = 0; i< category.size();i++){
            thucAnDanhMuc.addItem(category.get(i).getNameCategory());
        }
    }
    
    public void resetInFoFood(){
        thucAnId.setText("");
        thucAnNameFood.setText("");
        thucAnDanhMuc.setSelectedIndex(0);
        thucAnGia.setValue(0);
    }
    public boolean checkInFoFood(Food inFoFood){
        if(inFoFood.getId() == -1 ||
                inFoFood.getNameFood().equals("")||
                inFoFood.getIdCategory() == 0 ||
                inFoFood.getPrice()< 0){
            JOptionPane.showConfirmDialog(rootPane, "Ban chưa nhập đầy đủ thông tin");
            return false;
        }
        return true;
    }
    public Food getFoodByInFo(){
        Food foodNew = new Food(Integer.parseInt(thucAnId.getText()),
                thucAnNameFood.getText(),
                thucAnDanhMuc.getSelectedIndex(),
                Float.parseFloat(thucAnGia.getValue().toString()));
        return foodNew;
    }
